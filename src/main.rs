fn main() {
    let a = 5;
    let b = 10;

    println!("a == b: {}", a == b); // false
    println!("a != b: {}", a != b); // true
    println!("a > b: {}", a > b);   // false
    println!("a < b: {}", a < b);   // true
    println!("a >= b: {}", a >= b); // false
    println!("a <= b: {}", a <= b); // true
}
